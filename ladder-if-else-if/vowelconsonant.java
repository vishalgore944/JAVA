class vowelconsonant
{
    public static void main(String[] args)
    {
        char ch='a';
        if ((ch=='a') || (ch=='e') || (ch=='i') || (ch=='o') || (ch=='u'))
        {
            System.out.println("Entered character is a vowel");
        }
        else
        {
            System.out.println("Entered character is a consosnant");
        }
    }
}
